package tp03.ejercicio4;

public class Retardo {
	int mayor;
	int actual;
	
	public Retardo() {
		setMayor(0);
		setActual(0);
	}
	
	public int getMayor() {
		return mayor;
	}
	public void setMayor(int mayor) {
		this.mayor = mayor;
	}
	public int getActual() {
		return actual;
	}
	public void setActual(int actual) {
		this.actual = actual;
	}
}
